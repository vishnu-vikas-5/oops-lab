import java.lang.*;
public class math {
    public static void main(String[] args) {
        int a=3,b=12;
        System.out.println("Square root of the number" + Math.sqrt(a*b));
        System.out.println("Max: " + Math.max(a, b)); 
        System.out.println("Power  " + Math.pow(a, b));  
    }
    
}
